/* ── Carousel ── */
let carouselIndex = 0;

function moveCarousel(dir) {
  const track   = document.getElementById('carouselTrack');
  const cards   = track.querySelectorAll('.carousel-card');
  const isMobile = window.innerWidth <= 960;
  const visible  = isMobile ? 1 : 3;
  const max      = cards.length - visible;

  carouselIndex = Math.max(0, Math.min(carouselIndex + dir, max));

  const gap  = isMobile ? 0 : 24;
  const step = cards[0].offsetWidth + gap;

  track.style.transform = `translateX(-${carouselIndex * step}px)`;
}

window.addEventListener('resize', () => {
  carouselIndex = 0;
  const track = document.getElementById('carouselTrack');
  if (track) track.style.transform = 'translateX(0)';
});

/* ── Counter animation ── */
function animateCounter(el, target, suffix = '') {
  if (!el) return;
  let start = 0;
  const duration = 1500;
  const step = (timestamp) => {
    if (!start) start = timestamp;
    const progress = Math.min((timestamp - start) / duration, 1);
    el.textContent = Math.floor(progress * target) + suffix;
    if (progress < 1) requestAnimationFrame(step);
  };
  requestAnimationFrame(step);
}

const badgesEl = document.querySelector('.hero-badges');
if (badgesEl) {
  const badgeObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        animateCounter(document.querySelector('.hero-badge:nth-child(1) .hero-badge-num'), 365);
        animateCounter(document.querySelector('.hero-badge:nth-child(2) .hero-badge-num'), 5, '+');
        badgeObserver.disconnect();
      }
    });
  }, { threshold: 0.5 });
  badgeObserver.observe(badgesEl);
}

/* ── Contact form ── */
const contactForm = document.getElementById('contactForm');
if (contactForm) {
  contactForm.addEventListener('submit', async function(e) {
    e.preventDefault();
    const btn  = this.querySelector('.btn-submit');
    const data = new FormData(this);
    const t    = translations[currentLang];

    try {
      await fetch(this.action, {
        method: 'POST',
        body: data,
        headers: { Accept: 'application/json' }
      });
      btn.textContent      = currentLang === 'es' ? 'Mensaje enviado ✓' : 'Message sent ✓';
      btn.style.background = '#1a6e3a';
      btn.disabled         = true;
      this.reset();
      setTimeout(() => {
        btn.textContent      = t['contact.send'];
        btn.style.background = '';
        btn.disabled         = false;
      }, 4000);
    } catch {
      btn.textContent = currentLang === 'es' ? 'Error al enviar. Intente de nuevo.' : 'Error sending. Please try again.';
      setTimeout(() => { btn.textContent = t['contact.send']; }, 3000);
    }
  });
}

/* ── Hamburger menu ── */
const hamburger = document.getElementById('navHamburger');
const drawer    = document.getElementById('navDrawer');
const overlay   = document.getElementById('navOverlay');

function openMenu() {
  hamburger.classList.add('open');
  drawer.classList.add('open');
  overlay.classList.add('open');
  hamburger.setAttribute('aria-expanded', 'true');
  document.body.style.overflow = 'hidden';
}

function closeMenu() {
  hamburger.classList.remove('open');
  drawer.classList.remove('open');
  overlay.classList.remove('open');
  hamburger.setAttribute('aria-expanded', 'false');
  document.body.style.overflow = '';
}

hamburger.addEventListener('click', () => {
  drawer.classList.contains('open') ? closeMenu() : openMenu();
});

overlay.addEventListener('click', closeMenu);

document.querySelectorAll('.drawer-links a, .drawer-cta').forEach(link => {
  link.addEventListener('click', closeMenu);
});

document.addEventListener('keydown', (e) => {
  if (e.key === 'Escape') closeMenu();
});

